# How to run ACCENSE

# Not available in R, so follow steps below instead:

# Requires: Standalone ACCENSE application

# 1. Run ACCENSE application.
# 2. Select "Dimension reduction" under "Functions to run", set parameters, import data,
# select columns of markers, and click on "Run".
# 3. After dimensionality reduction is complete, proceed to the clustering step and
# visually check the t-SNE projection.
# 4. To perform clustering, select "Classification and colouring", and choose either 
# k-means, DBSCAN, or manual clustering. After clustering is complete, the output file
# "accense_output.csv" will be automatically updated to include the cluster labels.
